/*
 * 이 클래스는 Java 프로그램의 시작점을 정의합니다.
 * main 메서드는 프로그램 실행 시 가장 먼저 호출되는 메서드입니다.
 */

/**
 * Hello 클래스는 간단한 예제 프로그램입니다.
 * 이 프로그램은 main 메서드를 사용하여 콘솔에 "Hello, Java"를 출력합니다.
 */
public class Hello {
    /**
     * main 메서드는 Java 애플리케이션의 진입점입니다.
     * 이 메서드는 콘솔에 "Hello, Java"라는 메시지를 출력합니다.
     * @param args 명령행 인수 (이 프로그램에서는 사용되지 않음)
     */
    public static void main(String[] args) {
        // "Hello, Java"라는 메시지를 콘솔에 출력합니다.
        System.out.println("Hello, Java");
    }
}
